// switch case is just like IF eelse statement does the same work just syntax little bit different 

let month = 7;
switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    default:
        console.log("Some Other Month");
        break;
}